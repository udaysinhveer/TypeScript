
// enum is a sequence of constant values

enum Gender{
    Male,
    Female,
}
console.log(Gender.Male);  // it will return 0
console.log(Gender.Female); // it will return 1

// another way to access
console.log(Gender[0]); // it will return Male,
console.log(Gender[1]); // it will return Female,



enum Weekends {
    saturday = 6,
    sunday = 7,
}
console.log(Weekends[6]);
console.log(Weekends[7]);

